            </div>
          </ValidationObserver>
        </div>
      </div>
      <div id="output">
        <p id="serch_tittle">
          検索結果
        </p>
        <div>
          <table id="table">
            <tr>
              <th :class="sortedClass('name')" @click="sortBy('name')">
                ユーザー名
              </th>
              <th :class="sortedClass('created_at')" @click="sortBy('created_at')">
                登録日時
              </th>
              <th :class="sortedClass('updated_at')" @click="sortBy('updated_at')">
                更新日時
              </th>
              <th>参照</th>
            </tr>
            <tbody v-if="isActive">
              <tr v-for="user in eventedAction" :key="user.id">
                <td>
                  {{ user.name }}
