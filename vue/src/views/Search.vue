            <button id="reset" @click="resetting()">
              リセット
            </button>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import Header from '../views/Header.vue'

export default {
  name: 'Search',
  components: {
    Header
  },
  data () {
    return {
      // user_id: '',
      // name: '',
      // created_at: '',
      // updated_at: '',
      updatedFrom: null,
      updatedTo: null,
      isActive: false,
      isOpenUser: false,
      isOpenUpdata: false,
      isOpenSearch: true,
      // デフォルトを降順にする
      sortDesc: true,
      // 検索条件だけに沿った配列
      // eventedAction: [],
      // users: [
      //   {
      //     user_id: 1,
      //     name: 'ishida',
      //     created_at: '2020/05/01',
      //     updated_at: '2020/05/13'
      //   },
      //   {
      //     user_id: 2,
      //     name: 'sekiguti',
      //     created_at: '2020/05/03',
      //     updated_at: '2020/05/10'
      //   },
      //   {
      //     user_id: 3,
      //     name: 'sato',
      //     created_at: '2020/05/02',
      //     updated_at: '2020/05/11'
      //   }
      // ],
      sort: {
        isAct: false,
        key: ''
      },
      searchUser: '',
      searchDay: ''
    }
  },

  computed: {
    eventedAction () {
      const list = this.users.slice()

      if (this.sort.key) {
        list.sort((a, b) => {
          a = a[this.sort.key]
          b = b[this.sort.key]
          return (a === b ? 0 : a > b ? 1 : -1) * (this.sort.isAsc ? 1 : -1)
        })
      }

      return list
    },
    user_id () {
      // return this.$store.state.SearchGraph.user_id
      return this.user_id
    },
    name () {
      // return this.$store.state.SearchGraph.name
      return this.name
    },
    created_at () {
      // return this.$store.state.SearchGraph.created_at
      return this.created_at
    },
    updated_at () {
      // return this.$store.state.SearchGraph.updated_at
      return this.updated_at
    }
  },

  methods: {
    searchUserName () {
      this.isOpenSearch = false
      this.isOpenUser = true
    },
    searchUpdate () {
      this.isOpenSearch = false
      this.isOpenUpdata = true
    },
    search_active () {
      this.name = ''
      this.user_id = ''
      this.updated_at = ''
      this.created_at = ''
      this.isActive = true
      this.$store.dispatch(
        //  storeのactionsを呼び出すss
        'searchGraph/searchGraphs'
      )
    },
    returnScreen () {
      this.isOpenSearch = true
      this.isOpenUpdata = false
      this.isOpenUser = false
    },
    clear () {
      this.searchUser = ''
      this.updatedFrom = ''
      this.updatedTo = ''
    },
    sortBy (key) {
      this.sort.isAsc = this.sort.key === key ? !this.sort.isAsc : false
      this.sort.key = key
    },
    sortedClass (key) {
      return this.sort.key === key ? `sorted ${this.sort.isAsc ? 'asc' : 'desc'}` : ''
    },
    resetting () {
      this.sort.key = ''
      this.sort.isAsc = false
      this.searchUser = ''
      this.updatedFrom = ''
      this.updatedTo = ''
      this.isActive = false
      this.isOpenUser = false
      this.isOpenUpdata = false
      this.isOpenSearch = true
    }
  }
}
</script>

